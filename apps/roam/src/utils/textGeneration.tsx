export function generateRandomSentence(
  wordList: string[],
  wordCount: number,
): string {
  let sentence = "";
  for (let i = 0; i < wordCount; i++) {
    const randomIndex = Math.floor(Math.random() * wordList.length);
    sentence += wordList[randomIndex] + " ";
  }
  sentence = sentence.charAt(0).toUpperCase() + sentence.slice(1).trim() + ".";
  return sentence;
}

export function generateRandomSentences(
  count: number,
  wordsPerSentence: number,
): string[] {
  const sentences: string[] = [];
  for (let i = 0; i < count; i++) {
    sentences.push(generateRandomSentence(sampleWordList, wordsPerSentence));
  }
  return sentences;
}
